def decide_tone_index(repeat_count: int) -> int:
    """
    반복 횟수 → 톤 단계
    0회: 0
    1회: 1
    2회 이상: 2
    """
    if repeat_count <= 0:
        return 0
    if repeat_count == 1:
        return 1
    return 2
